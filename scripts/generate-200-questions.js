// 计数器和输出
const counters = {};
const questions = allRaw.map(q => {
  const prefix = typePrefix[q.type];
  if (!counters[q.type]) counters[q.type] = 0;
  counters[q.type]++;
  const id = `${prefix}_${String(counters[q.type]).padStart(3, '0')}`;
  return {
    id,
    type: q.type,
    format: q.format,
    difficulty: q.difficulty,
    question: q.question,
    options: q.options || [],
    answer: q.answer,
    explanation: q.explanation,
    handbookSource: q.handbookSource || '',
    qsrc: 'auto',
    semester: 'all',
    status: 'approved',
    knowledgePath: typeKnowledge[q.type] || '基础知识'
  };
});

// 校验
let errors = [];
const ids = new Set();
for (const q of questions) {
  if (ids.has(q.id)) errors.push(`重复ID: ${q.id}`);
  ids.add(q.id);
  if (!q.question || q.question.includes('undefined')) errors.push(`${q.id}: 题干异常`);
  if (!q.answer || q.answer.trim() === '') errors.push(`${q.id}: 答案为空`);
  if (Array.isArray(q.options) && q.options.length > 0) {
    if (!q.options.some(o => o.correct)) errors.push(`${q.id}: 缺少正确选项`);
    const texts = q.options.map(o => o.text);
    if (new Set(texts).size !== texts.length) errors.push(`${q.id}: 选项重复`);
  }
  if (!q.explanation || q.explanation.length < 5) errors.push(`${q.id}: 解析过短`);
}

